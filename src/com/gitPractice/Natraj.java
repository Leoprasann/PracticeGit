package com.gitPractice;

public class Natraj {
	
	private Integer studentdi;
	private String studentname;
	private String studentlocation;
	public Integer getStudentdi() {
		return studentdi;
	}
	public void setStudentdi(Integer studentdi) {
		this.studentdi = studentdi;
	}
	public String getStudentname() {
		return studentname;
	}
	public void setStudentname(String studentname) {
		this.studentname = studentname;
	}
	public String getStudentlocation() {
		return studentlocation;
	}
	public void setStudentlocation(String studentlocation) {
		this.studentlocation = studentlocation;
	}
	@Override
	public String toString() {
		return "Natraj [studentdi=" + studentdi + ", studentname=" + studentname + ", studentlocation="
				+ studentlocation + "]";
	}
	
	

}
